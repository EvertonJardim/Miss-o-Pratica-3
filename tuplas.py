# Criação da tupla 'primeira_tupla'
primeira_tupla = (1, 2, 3, 4, "Olá, tupla")

# Imprimindo o conteúdo da tupla
print("Conteúdo da primeira_tupla:", primeira_tupla)

# Imprimindo o índice do elemento 4 usando o método 'index'
indice_4 = primeira_tupla.index(4)
print("Índice do elemento 4 na tupla:", indice_4)

# Verificando se a tupla contém o elemento 3
contém_3 = 3 in primeira_tupla
print("A tupla contém o elemento 3:", contém_3)

# Verificando se a tupla contém o elemento 33
contém_33 = 33 in primeira_tupla
print("A tupla contém o elemento 33:", contém_33)
